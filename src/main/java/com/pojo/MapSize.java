package com.pojo;

public class MapSize {
	private int row;
	private int col;

	public int getRow() {
		return row;
	}

	public int getCol() {
		return col;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public void setCol(int col) {
		this.col = col;
	}

	@Override
	public String toString() {
		return "MapSize [row=" + row + ", col=" + col + "]";
	}

	
	
}
